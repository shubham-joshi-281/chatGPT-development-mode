// File Imports
import JWT from "jsonwebtoken";

// Generates Token
const reqiureSignIn = async (req, res, next) => {
  try {
    const decode = JWT.verify(
      req.headers.authorization,
      process.env.JWT_SECRET
    );
    req.user = decode;
    next();
  } catch (error) {
    console.log(error);
  }
};

export default reqiureSignIn;
